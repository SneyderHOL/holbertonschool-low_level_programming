#include "3-calc.h"
#include <stdio.h>
/**
 *op_add - function that adds two numbers
 *@a: integer argument
 *@b: integer argument
 *Return: a + b
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 *op_sub - function that substract two numbers
 *@a: integer argument
 *@b: integer argument
 *Return: a - b
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 *op_mul - function that multiply two numbers
 *@a: integer argument
 *@b: integer argument
 *Return: a * b
 */

int op_mul(int a, int b)
{
return (a * b);
}
/**
 *op_div - function that divide two numbers
 *@a: integer argument
 *@b: integer argument
 *Return: a / b
 */

int op_div(int a, int b)
{
return (a / b);
}
/**
 *op_mod - function that gets the module of a number
 *@a: integer argument
 *@b: integer argument
 *Return: a % b
 */

int op_mod(int a, int b)
{
return (a % b);
}
